fun main(){
    //arithmetic operators
    var num1=60
    var num2=20
    println(num1+num2)
    println(num1-num2)
    println(num1*num2)
    println(num1/num2)
    println(num1%num2) //this is a modulus(remainder)

    //Assignment operators
    var x=6
    x+=5
    println(x)

    var y=10
    y-=3
    println(y)

    var n=10
    n*=3
    println(n)

    var m=9
    m/=3
    println(m)


    var t=68
    t%=5
    println(t)

    //comparison operator
    var b=20
    println(b==2)
    println(b<2)
    println(b>2)
    println(b>=2)
    println(b<=2)

    //logical operators
    var mylogic=80
    println(mylogic<50 && mylogic==80) //and
    println(mylogic<50 || mylogic==80) //or
    println(!(mylogic<50 || mylogic==80)) //not





}